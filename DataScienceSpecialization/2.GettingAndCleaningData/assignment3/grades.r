grades <- function() {
  library("stringr")
  data <- data.frame()
  for (a1 in 2:10) {
    for (a2 in 2:10) {
      for (b1 in 2:10) {
        for (b2 in 2:10) {
          if (abs(a1 - a2) >= 1 && abs(b1 - b2) >= 1) {
            new_avg <- mean(sort(c(a1, a2, b1, b2))[2:3])
            old_avg <- mean(c(b1, b2))
            diff <- abs(old_avg - new_avg)
            text <- str_interp("Old marks: (${a1},${a2}) - New marks: (${b1},${b2})")
            data <- rbind(data, data.frame(new_avg, old_avg, diff, text))
          }
        }
      }
    }
  }
  data
}

